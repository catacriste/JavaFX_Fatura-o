package application;

public class Cliente {
	private int codCivil;
	private String Nome;
	private int NIF;
	private String morada;
	private int NIB;
	private int NISS;
	/**
	 * @return the codCivil
	 */
	public int getCodCivil() {
		return codCivil;
	}
	/**
	 * @return the nome
	 */
	public String getNome() {
		return Nome;
	}
	/**
	 * @return the nIF
	 */
	public int getNIF() {
		return NIF;
	}
	/**
	 * @return the morada
	 */
	public String getMorada() {
		return morada;
	}
	/**
	 * @return the nIB
	 */
	public int getNIB() {
		return NIB;
	}
	/**
	 * @return the nISS
	 */
	public int getNISS() {
		return NISS;
	}
	/**
	 * @param codCivil the codCivil to set
	 */
	public void setCodCivil(int codCivil) {
		this.codCivil = codCivil;
	}
	/**
	 * @param nome the nome to set
	 */
	public void setNome(String nome) {
		Nome = nome;
	}
	/**
	 * @param nIF the nIF to set
	 */
	public void setNIF(int nIF) {
		NIF = nIF;
	}
	/**
	 * @param morada the morada to set
	 */
	public void setMorada(String Morada) {
		morada = Morada;
	}
	/**
	 * @param nIB the nIB to set
	 */
	public void setNIB(int nIB) {
		NIB = nIB;
	}
	/**
	 * @param nISS the nISS to set
	 */
	public void setNISS(int nISS) {
		NISS = nISS;
	}

}
